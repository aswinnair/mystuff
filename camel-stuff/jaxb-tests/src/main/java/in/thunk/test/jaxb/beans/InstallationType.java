public class InstallationType {
	String kernelFileUrl;
	String kernelLocation;
	
	String initrdFileUrl;
	String initrdFileLocation;
	
	String pxeConfigFileUrl;
	String pxeConfigFileLocation;
	
	public String getKernelFileUrl() {
		return kernelFileUrl;
	}
	public void setKernelFileUrl(String kernelFileUrl) {
		this.kernelFileUrl = kernelFileUrl;
	}
	public String getKernelLocation() {
		return kernelLocation;
	}
	public void setKernelLocation(String kernelLocation) {
		this.kernelLocation = kernelLocation;
	}
	public String getInitrdFileUrl() {
		return initrdFileUrl;
	}
	public void setInitrdFileUrl(String initrdFileUrl) {
		this.initrdFileUrl = initrdFileUrl;
	}
	public String getInitrdFileLocation() {
		return initrdFileLocation;
	}
	public void setInitrdFileLocation(String initrdFileLocation) {
		this.initrdFileLocation = initrdFileLocation;
	}
	public String getPxeConfigFileUrl() {
		return pxeConfigFileUrl;
	}
	public void setPxeConfigFileUrl(String pxeConfigFileUrl) {
		this.pxeConfigFileUrl = pxeConfigFileUrl;
	}
	public String getPxeConfigFileLocation() {
		return pxeConfigFileLocation;
	}
	public void setPxeConfigFileLocation(String pxeConfigFileLocation) {
		this.pxeConfigFileLocation = pxeConfigFileLocation;
	}	
	
	
}
